'''
hay 2 formas de imprimir las listas
la primera es por su nombre utilizando
'''
lista = ['A','B','C','D','E','F','G','H','Y','J']

print(lista)
print("forma 1\n")#Salto de linea

'''
la segunda forma es imprimir elemento por elemento
utilizando el ciclo for
'''
for iterando in lista:
    print(iterando)
    
print("forma 2\n")#Salto de linea
'''
por utltimo si queremos imprimir un elemento alado del otro
en un solo renglon lo hacemos asi
'''
for iterando in lista:
    print(iterando,end=' ')

print("\nforma 3")#Salto de linea